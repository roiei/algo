class Solution:
    def modifyString(self, s: str) -> str:
        pre = ''
        res = ''
        cur = 'a'

        for i, ch in enumerate(s):
            if '?' != ch:
                res += ch
                pre = ch
                continue

            while cur == pre or (i + 1 < len(s) and s[i + 1] == cur):
                cur = chr(ord(cur) + 1)

            res += cur
            pre = cur
            cur = chr(ord(cur) + 1)

        return res


